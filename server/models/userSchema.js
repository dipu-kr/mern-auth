const mongoose = require("mongoose");

//---------- create user schema-------
const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  tokens: [
    {
      token: {
        type: String,
        required: true,
      },
    },
  ],
});

// -------------create model--------------
const userdb = new mongoose.model("users", userSchema);

module.exports = userdb;
